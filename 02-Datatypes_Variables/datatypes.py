#DATA TYPES:


# int → whole numbers (no decimal). Example: x = 10

# float → numbers with decimal point. Example: pi = 3.14

# str → text/characters inside quotes. Example: name = "X"

# bool → logical True/False values. Example: is_coder = True

# list → ordered, changeable collection. Example: fruits = ["apple", "banana", "cherry"]

# tuple → ordered, unchangeable collection. Example: point = (3, 4, 5)

# set → unordered, unique elements. Example: nums = {1, 2, 3, 3}

# dict → key-value pairs (like a map). Example: student = {"name": "Ajay", "age": 21}


